package pipeline.persistence.domain;

public class Specialty {

	private Integer id;
	private String name;

	public Specialty() {
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
